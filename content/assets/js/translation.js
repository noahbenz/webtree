// Funktion zum Aktualisieren der Texte basierend auf der ausgewählten Sprache
const textElement = document.getElementById("text-writer");
const enText = "Individually for Your Success!";
const deText = "Individuell für Deinen Erfolg!";
let text;
let typingTimeout;
let isTyping = false;

function setText(language) {
    textElement.textContent = ""; 
    if (language === 'en') {
        text = enText;
    } else {
        text = deText;
    }
}

function typeWriter(text, index) {
    if (index < text.length && isTyping) {
        textElement.textContent += text.charAt(index);
        index++;
        typingTimeout = setTimeout(function () {
            typeWriter(text, index);
        }, 60); 
    } else {
        isTyping = false; 
    }
}

function startAnimation(language) {
    setText(language);
    if (isTyping) {
        clearTimeout(typingTimeout); 
        isTyping = false;
    }
    setTimeout(function () {
        isTyping = true;
        typeWriter(text, 0); 
    }, 2400); 
}




var enButton = document.getElementById('en-link');
var deButton = document.getElementById('de-link');

function updateTexts(language) {
    var elements = document.querySelectorAll('[data-lang]');
    elements.forEach(function(element) {
        var key = element.getAttribute('data-lang');
        element.textContent = texts[language][key];
    });

    // Überprüfen, ob die Sprache tatsächlich geändert wurde
    var currentLanguage = localStorage.getItem('language');
    if (currentLanguage !== language) {
        // Speichern der Spracheinstellung im localStorage
        localStorage.setItem('language', language);
    }

    // Entferne zuerst alle Markierungen von Buttons

    // Markiere den entsprechenden Button, falls vorhanden
    if (language === 'en') {
        deButton.classList.remove('highlighted');
        enButton.classList.add('highlighted');
        document.getElementById("send-btn").value = "Send Message";
    } else {
      enButton.classList.remove('highlighted');
        deButton.classList.add('highlighted');
        document.getElementById("send-btn").value = "Nachricht senden";
    }
}

// Überprüfen und Laden der Spracheinstellung aus dem localStorage beim Laden der Seite
window.addEventListener('DOMContentLoaded', function() {
    var savedLanguage = localStorage.getItem('language');
    if (savedLanguage) {
        updateTexts(savedLanguage);
        startAnimation(savedLanguage); // Starten der Animation mit der gespeicherten Sprache
    } else {
        // Wenn keine Spracheinstellung im localStorage gefunden wurde, standardmässig auf Deutsch starten
        updateTexts('de');
        startAnimation('de'); // Starten der Animation mit Deutsch als Standard
    }
});

